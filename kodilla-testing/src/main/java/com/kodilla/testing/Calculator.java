package com.kodilla.testing;

public class Calculator {

    int a;
    int b;

    public Calculator() {
    }

    public int addAToB(int a, int b){
        return a + b;
    }

    public int subtractBFromA(int a, int b){
        return a - b;
    }

    public int getA() {
        return a;
    }

    public int getB() {
        return b;
    }
}
